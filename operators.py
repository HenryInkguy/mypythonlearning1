# Math Operators:

# "+" = Adding
# "-" = Subtracting
# "*" = Multiplication
# "/" = Division
# "//" = Floor division
# "%" = Modulus
# "**" = Exponential

# Comparison Operators:

# "==" = Equal to
# "!=" = Not equal to
# ">" = Greater than
# "<" = Less than
# ">=" = Greater than or equal to
# "<=" = Less than or equal to

# Logical Operators:

# "and" = Returns "True" if both conditions are true
# "or" = Returns "True"  if either of the conditions are true
# "not" = Inverts the Boolean value

